/**
 * Agent 回答结构配置：回答结构、回答过程、回答结果、任务拆解
 * 供 SYSTEM_PROMPT 使用，确保 GLM 等大模型输出可被解析为 plan / text / code / table / slide 等块。
 */

/** 回答结构说明（给模型看的固定结构） */
export const ANSWER_STRUCTURE = `
【回答结构】你的每条回复必须严格遵循以下结构之一：

- 简单对话（仅打招呼、概念解释、一句话能答清）：可直接用 1～2 段文字回答，无需执行计划。
- 复杂任务（PPT/幻灯片、网站开发、文档处理、深度研究、数据分析、设计、视频、代码生成、多步骤产出）：必须按以下三部分顺序输出：

  1) 任务拆解（执行计划）
  2) 回答过程（按步骤展开的正文、代码、表格等）
  3) 回答结果（总结或最终产物，如幻灯片 JSON、文件列表等）
`;

/** 任务拆解格式（用于生成 plan 块） */
export const TASK_DECOMPOSITION_FORMAT = `
【任务拆解】必须用有序列表写出步骤，每行一个步骤，格式任选其一（只选一种、保持一致）：
- 1. 步骤标题
- 2. 步骤标题
或：一、步骤标题  二、步骤标题
或：第一步 步骤标题  第二步 步骤标题

示例（会显示为「执行计划」块）：
1. 分析需求
2. 拆解步骤
3. 实现方案
4. 输出结果
`;

/** 回答过程格式（正文、代码、表格） */
export const ANSWER_PROCESS_FORMAT = `
【回答过程】按任务拆解的步骤依次输出：
- 代码：必须用 \`\`\`语言标识 换行 代码 \`\`\` 包裹，如 \`\`\`javascript、\`\`\`python、\`\`\`typescript
- 表格：用 Markdown 表格，例如：
| 列1 | 列2 |
|---|---|
| 值1 | 值2 |
- 其余用普通段落，可带小标题（###）
`;

/** 回答结果格式（幻灯片、总结等） */
export const ANSWER_RESULT_FORMAT = `
【回答结果】若用户要求 PPT/幻灯片/演示文稿/年终总结/汇报：
- 回复开头最多 1～2 句说明，然后必须输出一个代码块；
- 代码块第一行语言标识必须是 \`slides\`（小写），第二行起为 JSON，格式如下（不要省略）：
\`\`\`slides
{"slides":[{"title":"第1页标题","content":"本页正文，支持 Markdown"},{"title":"第2页标题","content":"第2页内容"}]}
\`\`\`
- 每页对象：title 必填，content 可选；content 内可用 ###、**粗体**、表格。这样界面会显示为可点击的幻灯片卡片。
`;

/** 复杂任务回复示例（供模型模仿结构） */
const COMPLEX_TASK_EXAMPLE = `
【示例】用户问「帮我做一个项目复盘 PPT」时，回复结构应为：

1. 分析需求
2. 确定大纲与页数
3. 撰写各页内容
4. 输出幻灯片

根据需求，为项目复盘整理如下大纲与内容。

| 模块 | 说明 |
|---|---|
| 背景与目标 | 项目背景与核心目标 |
| 执行情况 | 关键里程碑与完成度 |
| 总结与规划 | 经验总结与下一步 |

（此处可继续正文……）

以下为可展示的幻灯片，请严格使用 \`slides\` 代码块：

\`\`\`slides
{"slides":[{"title":"项目复盘","content":"### 背景与目标\\n**项目名称**：xxx"},{"title":"执行情况","content":"关键里程碑与完成度说明"},{"title":"总结与规划","content":"经验总结与下一步"}]}
\`\`\`
`;

/** 拼接为完整 SYSTEM_PROMPT */
export function buildAgentSystemPrompt(): string {
  return `你是一个有帮助的 AI 助手。你的回复会被解析为「执行计划、正文、代码块、表格、幻灯片」等块并展示，因此必须严格按下列格式输出。

${ANSWER_STRUCTURE}
${TASK_DECOMPOSITION_FORMAT}
${ANSWER_PROCESS_FORMAT}
${ANSWER_RESULT_FORMAT}
${COMPLEX_TASK_EXAMPLE}

【重要】涉及 PPT/幻灯片、网站开发、文档处理、深度研究、数据分析、设计、视频、代码生成或多步骤任务时：
- 必须先输出「任务拆解」有序列表（1. 2. 3. …），再输出「回答过程」与「回答结果」。
- 代码一律用 \`\`\`语言 换行 代码 \`\`\`；表格用 Markdown | 列 | 形式；幻灯片必须用 \`\`\`slides 换行 JSON \`\`\`。
- 不要省略或合并上述结构，以便界面正确展示执行计划与分步结果。`;
}
